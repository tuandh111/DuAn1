void filltable() {
        tblModel = (DefaultTableModel) tblUser.getModel();
        tblModel.setRowCount(0);
        try {
            List<KhachHangModel> list = khdao.select();
            for (KhachHangModel nv : list) {
                String NgaySinh = "";

                if (nv.getNgaySinh() != null) {
                    String dateString = nv.getNgaySinh();
                    Date date0 = new SimpleDateFormat("yyyy-MM-dd").parse(dateString);
                    NgaySinh = new SimpleDateFormat("dd-MM-yyyy").format(date0);
                }
                Object[] row = new Object[]{nv.getMaKH(),
                    nv.getTenKH(),
                    nv.getSDT(),
                    NgaySinh,
                    nv.getDiaChi().trim(),
                    nv.isGT(),
                    nv.getLoaiKH(),
                    nv.isTrangThai(),
                    nv.getMoTa(),
                    nv.getMaNV(),
                    nv.getHinh()};
                tblModel.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu");
        }
    }
